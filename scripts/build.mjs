import { cp, mkdir, readFile, rm, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const dist = path.join(root, 'dist');
const portable = path.join(root, 'release', 'WoW-Guide-Manager-0.1.0-portable');
const makePortable = process.argv.includes('--portable');

function stripExports(source) {
  return source.replaceAll('export const ', 'const ').replaceAll('export function ', 'function ');
}

function stripImports(source) {
  return source
    .split('\n')
    .filter((line) => !line.trimStart().startsWith('import '))
    .join('\n');
}

async function createBundle(target) {
  const guides = stripExports(await readFile(path.join(root, 'src/data/guides.js'), 'utf8'));
  const classGuides = stripExports(await readFile(path.join(root, 'src/data/classGuides.js'), 'utf8'));
  const classBuildGuides = stripExports(await readFile(path.join(root, 'src/data/classBuildGuides.js'), 'utf8'));
  const specGuides = stripExports(await readFile(path.join(root, 'src/data/specGuides.js'), 'utf8'));
  const dungeons = stripExports(await readFile(path.join(root, 'src/data/dungeonGuides.js'), 'utf8'));
  const assets = stripExports(await readFile(path.join(root, 'src/data/assetManifest.js'), 'utf8'));
  const subscriptions = stripExports(await readFile(path.join(root, 'src/data/subscriptions.js'), 'utf8'));
  const filters = stripExports(await readFile(path.join(root, 'src/lib/filterGuides.js'), 'utf8'));
  const app = stripImports(await readFile(path.join(root, 'src/app.js'), 'utf8'));
  const bundle = ['// Generated by scripts/build.mjs for file:// portable usage.', guides, classGuides, classBuildGuides, specGuides, dungeons, assets, subscriptions, filters, app].join('\n\n');

  await writeFile(path.join(target, 'src/app.bundle.js'), bundle, 'utf8');
}

async function copyApp(target, { bundleForFileProtocol } = { bundleForFileProtocol: false }) {
  await mkdir(target, { recursive: true });
  await cp(path.join(root, 'index.html'), path.join(target, 'index.html'));
  await cp(path.join(root, 'src'), path.join(target, 'src'), { recursive: true });
  await cp(path.join(root, 'assets'), path.join(target, 'assets'), { recursive: true });

  if (bundleForFileProtocol) {
    const indexPath = path.join(target, 'index.html');
    const index = await readFile(indexPath, 'utf8');
    await writeFile(
      indexPath,
      index.replace('<script type="module" src="./src/app.js"></script>', '<script defer src="./src/app.bundle.js"></script>'),
      'utf8'
    );
    await createBundle(target);
  }
}

async function main() {
  await rm(dist, { recursive: true, force: true });
  await copyApp(dist, { bundleForFileProtocol: true });

  if (makePortable) {
    await rm(portable, { recursive: true, force: true });
    await copyApp(path.join(portable, 'app'), { bundleForFileProtocol: true });
    await writeFile(
      path.join(portable, 'WoW Guide Manager.cmd'),
      '@echo off\r\nsetlocal\r\nstart "WoW Guide Manager" "%~dp0app\\index.html"\r\n',
      'utf8'
    );
    await writeFile(
      path.join(portable, 'README-PORTABLE.txt'),
      'WoW Guide Manager portable MVP\r\n\r\nStart: Doppelklicke "WoW Guide Manager.cmd" oder öffne app\\index.html in einem modernen Browser.\r\nAlle Dateien bleiben im portablen Ordner.\r\n',
      'utf8'
    );
  }

  if (!existsSync(path.join(dist, 'index.html'))) {
    throw new Error('Build failed: dist/index.html was not created.');
  }

  if (!existsSync(path.join(dist, 'src/app.bundle.js'))) {
    throw new Error('Build failed: dist/src/app.bundle.js was not created.');
  }

  console.log(makePortable ? `Portable build created: ${portable}` : `Build created: ${dist}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
